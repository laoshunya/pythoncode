# list和tuple

# list
names = ["Mark","Jack","Bob"]
print(names)
print(names[2])
# 添加元素
names.append("Lucy")
print(names)
names.insert(0,"Noya")
print(names)
# 删除元素
names.pop()
print(names)
names.pop(0)
print(names)
names.remove("Jack")
print(names)
# 替换元素
names[0] = "Noya"
print(names)

